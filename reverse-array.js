// We are declaring a new array called 'numbers' that contains the numbers 20, 50, 30, 60, and 200.

let numbers = [20, 50, 30, 60, 200];

// We are using the reverse() method to reverse the 'numbers' array in place, so the original array is now reversed.

numbers.reverse();

// We are outputting the original and reversed 'numbers' arrays using the console.log() method, so we can see the changes.

console.log(numbers);

// Output: [200, 60, 30, 50, 20]

